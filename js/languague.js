const link = document.getElementById("resumeLink");

function changeLanguage(lang) {
    // Cambiar el idioma en los elementos de la página
    document.querySelectorAll("[data-translate]").forEach(element => {
        const key = element.getAttribute("data-translate");
        if (translations[lang] && translations[lang][key]) {
            // Mantener el icono y solo cambiar el texto después de él
            const icon = element.querySelector("i"); // Encuentra el ícono
            const translatedText = translations[lang][key]; // Obtiene el texto traducido

            // Limpia el contenido original del elemento
            element.innerHTML = ""; 

            // Vuelve a agregar el icono si existe
            if (icon) {
                element.appendChild(icon);
            }

            // Usamos innerHTML para insertar el texto traducido con etiquetas HTML
            element.innerHTML += translatedText;  // Agrega el contenido traducido con HTML
        }
    });

    if (lang === "en") {
        link.href = "./resume/Fernando-Mora-EN.pdf"
    } else {
        link.href = "./resume/Fernando-Mora-ES.pdf"
    }

    const typedElementContainer = document.querySelector('.container_itype');
    
    // Eliminar el 'span' con la clase 'iTyped' si existe
    const typedElement = typedElementContainer.querySelector('.iTyped');
    // const cursorElement = typedElementContainer.querySelector('.ityped-cursor');
    if (typedElement) {
        typedElement.remove(); 
        // cursorElement.remove();
    }

    // Crear un nuevo 'span' y agregarlo al contenedor
    const newTypedElement = document.createElement('span');
    newTypedElement.classList.add('iTyped');
    typedElementContainer.appendChild(newTypedElement);


    // Inicializar ityped con las nuevas cadenas de texto según el idioma seleccionado
    setTimeout(() => {
        window.ityped.init(newTypedElement, {
            strings: translations[lang].typedStrings,
            loop: true,
            typeSpeed: 100, // Puedes ajustar la velocidad de tipeo si es necesario
            backSpeed: 50,  // Puedes ajustar la velocidad de borrado si es necesario
            cursor: "|",  // Especifica el cursor si lo necesitas
        });
    }, 50);

}

// Cargar idioma por defecto
document.addEventListener("DOMContentLoaded", () => {
    changeLanguage("en");  // O el idioma predeterminado
});


// Escuchar cambios en los radio buttons para cambiar el idioma
document.querySelectorAll(".language-switcher").forEach(input => {
    input.addEventListener("change", function () {
        changeLanguage(this.value);  // Cambiar idioma cuando el usuario selecciona un radio button
    });
});
